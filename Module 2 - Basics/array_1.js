// Arrays provides you an ordered collection of data

var arr = [12, "Neeraj", true, 12.4]  // this is how we initiallize an array []

console.log(arr)  // output : [12, "Neeraj", true, 12.4] 
console.log(arr[0])  // 12
console.log(arr[3])  // 12.4

// accessing value
let a = arr[2];
console.log(a) // true


// replacing the value
arr[1] = "Ferrari"
console.log(arr)  // [ 12, 'Ferrari', true, 12.4 ]

console.log(arr.length) // output : 4



