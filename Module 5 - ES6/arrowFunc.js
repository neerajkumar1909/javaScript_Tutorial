/* Normal Function
let test1 = function () {
    console.log(1)
}

let test2 = function(a) {
    console.log(a + 3)
}

let test3 = function (a, b) {
    console.log(a + b)
}

test1();
test2(2);
test3(4, 5);
*/


/* Arrow function
let test1 = () => {
    console.log(1)
}

let test2 = (a) => {
    console.log(a + 3)
}

let test3 = (a, b) => {
    console.log(a + b)
}

test1();
test2(2);
test3(4, 5);
*/


// Arrow function in short

let test1 = () => console.log(1)


let test2 = a => console.log(a + 3)


let test3 = (a, b) => console.log(a + b)


test1();
test2(2);
test3(4, 5);