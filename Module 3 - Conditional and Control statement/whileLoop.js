// while loop  ================================================

// A while loop evaluated the condition inside the parenthesis()
// If the condition eveluates to true, the code inside the while loop is execeuted
// The condition is eveluated again
// This process continues until the condition is false
// When the condition evaluates to false, the loop stops

var i = 1;

while (i <= 10) {
    console.log(i);
    i++;
}